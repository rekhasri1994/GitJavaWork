
public class Customer4 {
	
	    
		private String name;
		private String pswd;
		
		public Customer4() {
			super();
		}

		public Customer4(String name, String pswd) {
			super();
			this.name = name;
			this.pswd = pswd;
		}

		public String getName() {
			return name;
		}

		public void setName(String name) {
			this.name = name;
		}

		public String getPswd() {
			return pswd;
		}

		public void setPswd(String pswd) {
			this.pswd = pswd;
		}
		

	}


